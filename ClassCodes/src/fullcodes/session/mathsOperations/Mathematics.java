public class Mathematics {


    public static void main(String args []){
        int a = 12;
        int b = 2;

        //Add
        System.out.println(a + b);

        //Division
        System.out.println(a / b);


        //Multiply
        System.out.println(a * b);

        //Subtraction
        System.out.println(a - b);


        //Modulus
        System.out.println(a % b);


        //Increment by 1
        System.out.println(++a);








        //Decrement by 1
        System.out.println(--b);

    }

}
